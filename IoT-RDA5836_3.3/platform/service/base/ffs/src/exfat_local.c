INT32 exfat_read_inode ( struct inode *inode)
{
    EXFAT_SB_INFO* exfat_sb = NULL;
    INT32 iResult = _ERR_FAT_SUCCESS;   
    UINT32 bclu;                                         // Cluster
    UINT16 bsec;                                        // Sector index in cluster.
    UINT16 soff;                                         // The first dir entry offset in sector.
    EXFAT_DIR_ENTRY* dent = NULL;
    EXFAT_FILE_DIR_ENTRY* file_entry;
    EXFAT_EXT_DIR_ENTRY* ext_entry;
    UINT16  ecount = 0;
    
    //struct inode* curi;
    //struct inode **prei;
  /*  if(bclu < 2)
    {        
        D( ( DL_FATERROR, "in exfat_read_inode. Invalid custer number:0x%x",bclu) );
        DSM_ASSERT(0, "in exfat_read_inode. Invalid custer number:0x%x",bclu);
    }
	*/
    exfat_sb = &(inode->i_sb->u.exfat_sb);
    if( NULL == inode)
    {
        D( ( DL_FATERROR, "in exfat_read_inode. inode is null \n") );
        return _ERR_FAT_PARAM_ERROR;
    }        
    file_entry = &inode->u.exfat_i.file_entry;
    ext_entry = &inode->u.exfat_i.ext_entry;  
    dent = DSM_MAlloc(EXFAT_DIR_ENTRY_COUNT*sizeof(EXFAT_DIR_ENTRY));
    if(!dent)
    {
        D( ( DL_FATERROR,"exfat_read_inode:malloc(%d) failed.",
                (EXFAT_DIR_ENTRY_COUNT*sizeof(EXFAT_DIR_ENTRY))));        
        return _ERR_FAT_MALLOC_FAILED;
    }
    DSM_MemSet(dent,0,(EXFAT_DIR_ENTRY_COUNT*sizeof(EXFAT_DIR_ENTRY)));
    // Read root inode 
    if(FS_ROOT_INO == inode->i_ino) 
    {                    
        file_entry->EntryType = EXFAT_ETYPE_FILE;
        file_entry->SeconCount = 1;
        file_entry->FileAttributes = ATTR_DIR;
        ext_entry->EntryType = EXFAT_ETYPE_EXT;
        ext_entry->GenSeconFlags = 0x3;
        ext_entry->NameLength = 0;
        ext_entry->NameHash = 0;
        ext_entry->ValidDataLength = ext_entry->DataLength = (exfat_sb->BytesPerSector*exfat_sb->SectorsPerCluster);
        ext_entry->FirstCluster = exfat_sb->RootDirectoryCluster;        
    }
    else
    {
        bclu = EXFAT_INO2CLUS(inode->i_ino);
        bsec = EXFAT_INO2SEC(inode->i_ino);
        soff = EXFAT_INO2EOFF(inode->i_ino);
           
        iResult = exfat_read_entries(inode, bclu, bsec, soff, dent, &ecount);
        
        if(_ERR_FAT_SUCCESS != iResult)
        {
            D( ( DL_FATERROR, "in exfat_read_inode. read entries error ErrorCode = %d\n",iResult) );
            goto _func_end;
        }
        DSM_MemCpy(file_entry,&dent[0],FAT_DIR_ENTRY_SIZE);
        DSM_MemCpy(ext_entry,&dent[1],FAT_DIR_ENTRY_SIZE);
    }
    inode->u.exfat_i.Dirty = DATA_CLEAN;
    inode->u.exfat_i.CurClus = ext_entry->FirstCluster;
    inode->u.exfat_i.ClusIndx = 0;
    inode->i_size = ext_entry->ValidDataLength;
    inode->i_mode = (file_entry->FileAttributes & 0xffff);
    inode->i_ctime = file_entry->CreatTime;
    inode->i_atime = file_entry->LATime;
    inode->i_mtime = file_entry->LMTime;
_func_end:
    if(dent)
    {
        DSM_Free(dent);
    }

    return iResult;
}


INT32 exfat_do_find_entry( struct inode * dir_i, struct dentry *de, UINT64 *ino)
{
    EXFAT_EXT_DIR_ENTRY* ext_entry;
    UINT32 bclu = 0;
    UINT16 bsec = 0;
    UINT16 eoff = 0;
    UINT64 curino = 0;
    UINT64 nexino = 0; 
    UINT16 ecount = 0; 
    INT32 result;
    INT32 ret;
    
    //
    // The 'append_data' is used to save the postion information for finding function
    // and it is made of three members: 1)begining cluster, 2)begining sector(based on cluster)
    // and 3) offset on the begining sector
    // 
    ext_entry = &dir_i->u.exfat_i.ext_entry;

    if(de->append_data)
    {
        bclu = EXFAT_INO2CLUS(de->append_data);
        bsec = EXFAT_INO2SEC(de->append_data);
        eoff = EXFAT_INO2EOFF(de->append_data);
    }
    else    
    {
        bclu = ext_entry->FirstCluster;
        bsec = 0;
        eoff = 0;	    
    }

    curino = EXFAT_CSO2INO(bclu,bsec,eoff);
    result = exfat_lookup_file(dir_i,de->name,&curino,&nexino,de->name,&ecount); 
    if(ERR_SUCCESS == result)
    {
        *ino = curino;        
         if (FS_FLAG_FIND_FILE == de->append_data2)
         {
            de->append_data = nexino;            
         }
        ret = ERR_SUCCESS;
    }
    else
    {
        D( ( DL_VFSERROR, "in exfat_do_find_entry.fat_lookup_entry error ErrorCode = %d\n",result) );
        ret = result;
    }
    return ret;
}


