INT32 exfat_is_formated(struct super_block *sb)
{
    UINT64 iSecOff = 0;
    UINT32 iFatSz = 0;
    UINT32 iFatIndex = 0;
    UINT32 i;
    UINT8* pBuff = NULL;
    UINT32 iFatSymbol = 0;
    INT32 iResult = 0;
    INT32 iRet = _ERR_FAT_HAS_FORMATED;
    UINT32 uDevNo;  
    EXFAT_SB_INFO* sb_info;
     
    // malloc the buffer to stored the fbr content.
    pBuff = FAT_SECT_BUF_ALLOC();
    if(NULL == pBuff)
    {
        D(( DL_FATERROR,"in exfat_is_formated,1.FAT_SECT_BUF_ALLOC() failed.")); 
        DSM_ASSERT(0,"in exfat_is_formated,1.FAT_SECT_BUF_ALLOC() failed.");
        return _ERR_FAT_MALLOC_FAILED;
    }      
    
    uDevNo = sb->s_dev; 
    sb_info = &sb->u.exfat_sb;
    // Read the FAT Region
    iSecOff = sb_info->PartitionOffset + sb_info->FatOffset;
    iFatIndex = 0;

    iFatSz = sb_info->FatLength;
    
    // only check the first fat table.
    while((iFatIndex < sb_info->NumberOfFats) && (_ERR_FAT_HAS_FORMATED == iRet))
    {     
        //DSM_MemSet(pBuff,0,DEFAULT_SECSIZE);
        for(i = 0; i < 1; i++)
        {
            iResult = DRV_BLOCK_READ( uDevNo, iSecOff, pBuff );
            if(_ERR_FAT_SUCCESS != iResult)
            {
                iRet = _ERR_FAT_READ_SEC_FAILED;
                D((DL_FATERROR, "in exfat_is_formated(), DRV_BLOCK_READ! ErrorCode = %d \n",iResult));
                break;
            }

            iFatSymbol = 0x0;                                      
            DSM_MemCpy(&iFatSymbol,pBuff,4);
            if (iFatSymbol == EXFAT_FAT_VALUE_MEDIA)//symbol:0xfffffff8
            {
                DSM_MemCpy(&iFatSymbol,pBuff + 4,4);
                if (iFatSymbol == EXFAT_FAT_VALUE_EOF)
                {
                    continue;
                }
            }
            
            D((DL_FATERROR, "in exfat_is_formated(), iFatSymbol(0x%x) error \n", iFatSymbol));
            iRet = _ERR_FAT_NOT_FORMAT;
            break;
        } 
        iSecOff += iFatSz;            
        iFatIndex ++;
    }        
        
    if(NULL != pBuff)
    {        
        FAT_SECT_BUF_FREE((SECT_BUF*)pBuff);
    }
    return iRet;
}


INT32 exfat_format(UINT32 dev_no, UINT64 total_sec, UINT64 partitionoffset, UINT8* vollab,UINT8* fsname,UINT8 opt)
{   
    UINT32 volserial;
    UINT32 iSecNum = 0;
    UINT32 iFatIndex = 0;
    UINT32 i,j;
    EXFAT_BOOT_RECORD sBR;
    EXFAT_EXTEND_BOOT_RECORD sEBR;
    EXFAT_OEM_RECORD sOR;
    UINT32 iCheckSum;
    UINT32 iUCTChecksum = 0;
    //region size
    UINT32 iReserveSz = 0;
    UINT32 iFatSz = 0;
    UINT32 iBitMapSz = 0;
    UINT32 iUCTSz = 0;
    UINT32 iRootDirSz = 0;
    UINT32 iBitMapClusCnt = 0;
    //region position
    UINT32 iFatOffset;
    UINT32 iClusterHeapOffset;
    UINT32 iBitMapOffset;
    UINT32 iUCTOffset;
    UINT32 iRootDirOffset;
    UINT32 iRootDirCluster;
    //region alignment
    UINT32 iReserveAlign;
    UINT32 iFatAlign;
    UINT32 iBitMapAlign;
    UINT32 iUCTAlign;
    // UINT32 iRootDirAlign;
    //
    UINT64 iDataSec;
    UINT32 iStartSec;    
    UINT32 iOffs;
    UINT32 iTmpSize = 0;
    UINT32 iSecPerClus = 1;
    UINT8  Powerof2;
    UINT32 iCountOfClus;

    UINT8* pBuff = NULL;
    UINT8* p = NULL;
    INT32 iResult;    
    INT32 iRet = _ERR_FAT_SUCCESS;
    FAT_CACHE* psFatCache = NULL;

    // Input parameter check.
    if((0 == total_sec) || ((0 != opt) &&(1 != opt)))
    {
         D( ( DL_FATERROR, "in exfat_format, iDiskTotSec = %d,opt = %d.\n",total_sec,opt) ); 
        return _ERR_FAT_PARAM_ERROR;
    }   
    if(NULL == vollab || NULL == fsname)
    {
        D( ( DL_FATERROR, "in exfat_format, vollab = %d,fsname = 0x%x.\n",vollab,fsname) ); 
        return _ERR_FAT_PARAM_ERROR;
    }

    // Malloc the buffer to stored the fbr content.
    pBuff = DSM_MAlloc(DEFAULT_SECSIZE);
	if(NULL == pBuff)
    {
        D(( DL_FATERROR,"in exfat_format,1.FAT_SECT_BUF_ALLOC() failed.")); 
        DSM_ASSERT(0,"in exfat_format,1.FAT_SECT_BUF_ALLOC() failed.");
        return _ERR_FAT_MALLOC_FAILED;
    }      
    // Clean up the cache of FAT if the cache device is formated device.
    psFatCache = fat_get_fat_cache(dev_no);
    if(NULL == psFatCache)
    {
        D((DL_FATERROR, "in exfat_format: fat cache is null,s_dev = 0x%x.\n", dev_no));
        DSM_ASSERT(0,"in exfat_format,1.FAT_SECT_BUF_ALLOC() failed.");
        iResult = _ERR_FAT_CACHE_IS_NULL;
        goto end;
    }
    else
    {
       psFatCache->iDirty = 0;
       psFatCache->iSecNum = 0;
       psFatCache->psFileInodeList = NULL;
    }
    DRV_SET_WCACHE_SIZE(dev_no,DEFAULT_SECSIZE*64);
    // Get EXFAT_FBR and other format parameter.
    volserial = DSM_GetRandNum();
    Powerof2 = exfat_GetSecNumPerClus(total_sec);
	iSecPerClus = 1 << Powerof2;

    iReserveSz = EXFAT_RESERVED_SECTOR_COUNT * DEFAULT_SECSIZE;
    iReserveAlign = total_sec < 8388608 ? EXFAT_FAT_ALIGNMENT_LENGTH_128SEC : EXFAT_FAT_ALIGNMENT_LENGTH_2048SEC;

    iFatOffset = ROUND_UP(iReserveSz , iReserveAlign) / DEFAULT_SECSIZE;
    iFatSz = exfat_GetFATSz(total_sec, iFatOffset, iSecPerClus) * DEFAULT_SECSIZE;
    iFatAlign = iReserveAlign;

    iClusterHeapOffset = iFatOffset + ROUND_UP(iFatSz , iFatAlign) / DEFAULT_SECSIZE;
    iDataSec = total_sec - iClusterHeapOffset;
    iCountOfClus = (UINT32)iDataSec / iSecPerClus;

    iBitMapOffset = iClusterHeapOffset;
    iBitMapSz = exfat_GetBitMapSz(iCountOfClus);
    iBitMapAlign = iSecPerClus * DEFAULT_SECSIZE;
    iBitMapClusCnt = DIV_ROUND_UP(iBitMapSz,iBitMapAlign);

    iUCTOffset = iBitMapOffset + ROUND_UP(iBitMapSz , iBitMapAlign) / DEFAULT_SECSIZE;
    iUCTSz = exfat_GetUCTSz();
    iUCTAlign = iSecPerClus * DEFAULT_SECSIZE;

    iUCTChecksum = exfat_UCTChecksum(g_UctTable,iUCTSz);

    iRootDirOffset = iUCTOffset + ROUND_UP(iUCTSz , iUCTAlign) / DEFAULT_SECSIZE;
    iRootDirSz = exfat_GetRootDirSz(iSecPerClus);
    // iRootDirAlign = iSecPerClus * DEFAULT_SECSIZE;
    iRootDirCluster = (iRootDirOffset - iBitMapOffset) / iSecPerClus + 2;

    exfat_GetBR(fsname, volserial,total_sec, partitionoffset, iFatOffset, iFatSz, iClusterHeapOffset, iCountOfClus, iRootDirCluster, &sBR);
    DSM_MemSet(pBuff,0,DEFAULT_SECSIZE);
	iStartSec = (UINT32)sBR.PartitionOffset + sBR.ClusterHeapOffset + \
        (sBR.RootDirectoryCluster - EXFAT_CLUSTER_DATA_START) * iSecPerClus;
     // Root Directory.    
    for(iOffs = 0; iOffs < iRootDirSz; iOffs ++)
    {        
        if(0 == iOffs)
        {
            exfat_SetRootDir(vollab,iBitMapSz,iBitMapClusCnt,iUCTSz,iUCTChecksum,pBuff);
        }
        else if(1 == iOffs)
        {
            DSM_MemSet(pBuff,0,DEFAULT_SECSIZE);
        }        
        
        iSecNum = iStartSec + iOffs;
        
        iResult = DRV_BLOCK_WRITE(dev_no,iSecNum,pBuff);
        if(_ERR_FAT_SUCCESS != iResult)
        {
            D((DL_FATERROR, "in exfat_format: DRV_BLOCK_WRITE error,iResult = %d,iSecNum = 0x%x.\n",iResult,iSecNum));
            iRet = _ERR_FAT_WRITE_SEC_FAILED;
            goto end;
        }
     }
    
    // UCT(UP-Case Table).
    iStartSec = (UINT32)sBR.PartitionOffset + sBR.ClusterHeapOffset + (EXFAT_FST_CLUSTER_UCT - EXFAT_CLUSTER_DATA_START) *\
                      iSecPerClus;
    for(iOffs = 0; iOffs < iSecPerClus; iOffs ++)
    {        
        iSecNum = iStartSec + iOffs;
        if(iOffs < iUCTSz/DEFAULT_SECSIZE)
        {
             DSM_MemCpy(pBuff,(UINT8*)g_UctTable + iOffs*DEFAULT_SECSIZE,DEFAULT_SECSIZE);
        }
        else if(iOffs == iUCTSz/DEFAULT_SECSIZE)
        {
             iTmpSize = (EXFAT_UCT_LENGTH - iOffs*DEFAULT_SECSIZE);
             DSM_MemSet(pBuff,0,DEFAULT_SECSIZE);
             DSM_MemCpy(pBuff,(UINT8*)g_UctTable + iOffs*DEFAULT_SECSIZE,iTmpSize);
        }
        else
        {
            DSM_MemSet(pBuff,0,DEFAULT_SECSIZE);
        }
        iResult = DRV_BLOCK_WRITE(dev_no,iSecNum,pBuff);
        if(_ERR_FAT_SUCCESS != iResult)
        {
            D((DL_FATERROR, "in exfat_format: DRV_BLOCK_WRITE error,iResult = %d,iSecNum = 0x%x.\n",iResult,iSecNum));
            iRet = _ERR_FAT_WRITE_SEC_FAILED;
            goto end;
        }
     }

     // AB(Allocation Bitmap).
    iStartSec = (UINT32)sBR.PartitionOffset + sBR.ClusterHeapOffset;
    for(iOffs = 0; iOffs < (iSecPerClus*iBitMapClusCnt); iOffs ++)
    {        
        iSecNum =iStartSec + iOffs;
        if(iOffs == 0)
        {
            DSM_MemSet(pBuff,0,DEFAULT_SECSIZE);
            exfat_SetBitMap(pBuff,iBitMapClusCnt);
        }
        if(iOffs == 1)
        {
            DSM_MemSet(pBuff,0,DEFAULT_SECSIZE);
        }
        iResult = DRV_BLOCK_WRITE(dev_no,iSecNum,pBuff);
        if(_ERR_FAT_SUCCESS != iResult)
        {
            D((DL_FATERROR, "in exfat_format: DRV_BLOCK_WRITE error,iResult = %d,iSecNum = 0x%x.\n",iResult,iSecNum));
            iRet = _ERR_FAT_WRITE_SEC_FAILED;
            goto end;
        }
     }

    
    // FAT.
    iStartSec = (UINT32)sBR.PartitionOffset + sBR.FatOffset;
    
    for(iFatIndex= 0; iFatIndex < sBR.NumberOfFats;iFatIndex ++)
    {
        for(iOffs = 0; iOffs < (iFatSz + DEFAULT_SECSIZE - 1)/DEFAULT_SECSIZE; iOffs ++)
        {        
            if(0 == iOffs)
            {
                exfat_SetFat(pBuff);
            }
            else if(1 == iOffs)
            {
                DSM_MemSet(pBuff,0,DEFAULT_SECSIZE);
            }
            else
            {
                //Do nothing.
            }
            iSecNum = iStartSec + iOffs;
            iResult = DRV_BLOCK_WRITE(dev_no,iSecNum,pBuff);
            if(_ERR_FAT_SUCCESS != iResult)
            {
                D((DL_FATERROR, "in exfat_format: DRV_BLOCK_WRITE error,iResult = %d,iSecNum = 0x%x.\n",iResult,iSecNum));
                iRet = _ERR_FAT_WRITE_SEC_FAILED;
                goto end;
            }
         }
    }
    
    // Reserved.
    iStartSec = (UINT32)sBR.PartitionOffset;
    DSM_MemSet(pBuff,0,DEFAULT_SECSIZE);
    for(iOffs = EXFAT_RESERVED_SECTOR_COUNT; iOffs < sBR.FatOffset; iOffs ++)
    {                
        iSecNum = iStartSec + iOffs;
        iResult = DRV_BLOCK_WRITE(dev_no,iSecNum,pBuff);
        if(_ERR_FAT_SUCCESS != iResult)
        {
            D((DL_FATERROR, "in exfat_format: DRV_BLOCK_WRITE error,iResult = %d,iSecNum = 0x%x.\n",iResult,iSecNum));
            iRet = _ERR_FAT_WRITE_SEC_FAILED;
            goto end;
        }
     }
    
    // MBR(Main Boot Region) and BBR(Backup Boot Region).

    // Set MBR/BBR buffer.
    DSM_MemSet(pBuff,0,DEFAULT_SECSIZE);
    // Write block.
    iStartSec = (UINT32)sBR.PartitionOffset;
    for(i = 0;i < 2; i++)
    {        
        p = pBuff;
        for(j = 0; j < EXFAT_MBR_SECTOR_COUNT; j++)
        {
            iSecNum = iStartSec + i*EXFAT_MBR_SECTOR_COUNT + j;
			DSM_MemSet(p,0,DEFAULT_SECSIZE);
			switch(j)
			{
				// 0 Boot Sector.
				case 0:
					DSM_MemCpy(p,&sBR,sizeof(EXFAT_BOOT_RECORD));
				    break;

                // 1-8 Extended Boot Sector.
				case 1:
				case 2:
				case 3:
				case 4:
				case 5:
				case 6:
				case 7:
				case 8:
					exfat_GetEBR(&sEBR);
					DSM_MemCpy(p,&sEBR,sizeof(EXFAT_EXTEND_BOOT_RECORD)); 
					break;

                // 9 Extended Boot Sector.
				case 9:
					exfat_GetOEM(&sOR);
					DSM_MemCpy(p,&sOR,sizeof(EXFAT_OEM_RECORD));
					break;

                // 10 Reserved.
				case 10:
					DSM_MemSet(p,0,DEFAULT_SECSIZE);
					break;

                // 11 Boot Checksum.
				case 11:
					iCheckSum = exfat_BootChecksum(p,(EXFAT_MBR_SECTOR_COUNT - 1)*DEFAULT_SECSIZE);
					exfat_SetBootChecksum(iCheckSum,p);
					break;
			}
            iResult = DRV_BLOCK_WRITE(dev_no,iSecNum,p);
            if(_ERR_FAT_SUCCESS != iResult)
            {
                D((DL_FATERROR, "in exfat_format: DRV_BLOCK_WRITE error,iResult = %d,iSecNum = 0x%x.\n",iResult,iSecNum));
                iRet = _ERR_FAT_WRITE_SEC_FAILED;
                goto end;
            }
        }
    }
	
#if 0
	iStartSec = 0;
	DSM_MemSet(pBuff,0,DEFAULT_SECSIZE);
	for(i = 0; i < EXFAT_HIDDEN_SECTOR_COUNT; i++)
	{
        iSecNum = iStartSec + i;
		iResult = DRV_BLOCK_WRITE(dev_no,iSecNum,pBuff);
        if(_ERR_FAT_SUCCESS != iResult)
        {
            D((DL_FATERROR, "in exfat_format: DRV_BLOCK_WRITE error,iResult = %d,iSecNum = 0x%x.\n",iResult,iSecNum));
            iRet = _ERR_FAT_WRITE_SEC_FAILED;
            goto end;
        }
	}
#endif

   end:
    if(NULL != pBuff)
    {
        DSM_Free(pBuff);
    }

    return iRet;
}


